/*===================================================
	USART1
===================================================*/

#define USART1_REC_LEN  			200  	//定义最大接收字节数 200
#define EN_USART1_RX 				1		//使能（1）/禁止（0）串口1接收
extern uint8_t  USART1_RX_BUF[USART1_REC_LEN];	//接收缓冲,最大USART_REC_LEN个字节.末字节为换行符 
extern uint16_t USART1_RX_STA;					//接收状态标记

void USART1_Init(uint32_t bound);
void USART1_Send_String(uint8_t *str);
void USART1_Postback(void);

/*===================================================
	USART3
===================================================*/

#define USART3_REC_LEN  			200  	//定义最大接收字节数 200
#define EN_USART3_RX 				1		//使能（1）/禁止（0）串口1接收
extern uint8_t  USART3_RX_BUF[USART3_REC_LEN];	//接收缓冲,最大USART_REC_LEN个字节.末字节为换行符
#define USART3_RX_STA_REC_END			(0X8000)		// bit15		接收完成一包数据
#define USART3_RX_STA_HAS_REC			(0X4000)		// bit14		有接收
#define USART3_RX_STA_OVERFLOW			(0X3000)		// bit13~12		缓冲区溢出次数
#define USART3_RX_STA_REC_NUM			(0X0FFF)		// bit11~0		缓冲区接收字符数
extern uint16_t USART3_RX_STA;					//接收状态标记

void USART3_Init(uint32_t bound);
void USART3_Send_Char(uint8_t chr);
void USART3_Send_Num(uint16_t num);
void USART3_Send_String(uint8_t *str);
uint8_t USART3_Get_Char(uint16_t addr);
uint8_t *USART3_Get_String(void);
void USART3_Postback(void);
